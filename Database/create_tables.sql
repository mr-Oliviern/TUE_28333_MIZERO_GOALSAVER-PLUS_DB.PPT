CREATE TABLE audit_log (
    audit_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id VARCHAR2(50),
    action_type VARCHAR2(20),
    table_name VARCHAR2(50),
    record_id VARCHAR2(50),
    action_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    status VARCHAR2(20)
);


CREATE TABLE public_holidays (
    holiday_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    holiday_date DATE NOT NULL,
    description VARCHAR2(100)
);


  CREATE TABLE "PROJECT"."ACCOUNT" 
   (	"ACCOUNT_ID" NUMBER(12,0), 
	"ACCOUNT_NO" VARCHAR2(30 BYTE) NOT NULL ENABLE, 
	"MEMBER_ID" NUMBER(10,0), 
	"ACCOUNT_TYPE" VARCHAR2(30 BYTE), 
	"OPEN_DATE" DATE DEFAULT SYSDATE, 
	"CLOSE_DATE" DATE, 
	"CURRENCY" VARCHAR2(3 BYTE) DEFAULT 'RWF', 
	"STATUS" VARCHAR2(20 BYTE) DEFAULT 'OPEN', 
	"ACCOUNT_AMOUNT" NUMBER(15,2) DEFAULT 0 NOT NULL ENABLE, 
	 CHECK (ACCOUNT_TYPE IN ('SAVINGS','GOAL','FIXED')) ENABLE, 
	 PRIMARY KEY ("ACCOUNT_ID"));

